n = int(input("Введите размер шоколадки по горизонтали: "))
m = int(input("Введите размер шоколадки по вертикали: "))
k = int(input("Сколько хотите отломать: "))

if k <= m * n:
    print("Получится" if k % m == 0 or k % n == 0 else "Не получится")
else:
    print("Вы хотите отломать больше, чем есть кусочков")
